		Dados dados = new Dados();
                String acao = request.getParameter("acao");
		dados.setOp1(request.getParameter("op1"));
                dados.setAcao(request.getParameter("acao"));
                if(acao.equals(" = ")) {
		dados.setOp2(request.getParameter("op2"));
		dados.setOperacao(request.getParameter("operacao"));
                    dados.setOp(true);
                    request.setAttribute("dados", dados);
                    request.getRequestDispatcher("servlet/binario").forward(request, response);
                }
		else {
                    dados.setOp(false);
                    request.setAttribute("dados", dados);
                    request.getRequestDispatcher("servlet/unario").forward(request, response);
                }
